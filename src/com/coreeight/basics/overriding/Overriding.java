package com.coreeight.basics.overriding;

public class Overriding {

    // Overriding - Suppose Child inherit Parent class and if Child class declare a method as Parent method signature is called Overriding.
    /**
     * Rule 1 - Assume Parent have a method and it should not be a private.
     * Rule 2 - Parent method should not be final.
     * Rule 3 - if Child extends Parent and Child declared a method as Parent method signature.
     *          1) Method name should be same in both
     *          2) Arguments should be same in both
     *          3) Child method Access specifiers, return type and throws are will follow Parent method
     *          4) Child should not decrease visibility for method - ex:- Child_AccessSpecifier.java
     */
}
